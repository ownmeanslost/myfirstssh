package com.myfirstssh.resume.model;

public class ResumeInfo {
	String Guid;
	String resumeName;
	String userjavabean;
	String beanjson;
	String filename;
	String filepath;
	public String getGuid() {
		return Guid;
	}
	public void setGuid(String guid) {
		Guid = guid;
	}
	public String getResumeName() {
		return resumeName;
	}
	public void setResumeName(String resumeName) {
		this.resumeName = resumeName;
	}
	public String getUserjavabean() {
		return userjavabean;
	}
	public void setUserjavabean(String userjavabean) {
		this.userjavabean = userjavabean;
	}
	public String getBeanjson() {
		return beanjson;
	}
	public void setBeanjson(String beanjson) {
		this.beanjson = beanjson;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getFilepath() {
		return filepath;
	}
	public void setFilepath(String filepath) {
		this.filepath = filepath;
	}
	
	
}
